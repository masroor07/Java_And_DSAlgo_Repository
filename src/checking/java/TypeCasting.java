package checking.java;

public class TypeCasting {

	public static void main(String[] args) {
		//manual type casting:
		//declare a variable with datatype double 
		double mydouble = 3.124;
		//store the value of variable mydouble , typecasting it into integer data type
		int myint= (int) mydouble;
		//prints myint
		System.out.println(myint);
		//prints mydouble
		System.out.println(mydouble);

	}

}
